//動作			:ステージデータをstageDrawに送る
/////////////////////////////////////////////////////
void GameScene::SetStageNum(int num)
{
	// ステージの総数より大きな数字が与えられないように設定
	if (MaxStageNum > num) stageNum = num;

	// StageDrawにステージデータを渡す
	stageDraw->SetStageData(StageInpoter::Instance()->GetStageData()[num]);
}

